const planeta = 0;

switch(planeta){
    case 0:
        console.log('Mercurio');
        break;
    case 1:
        console.log('Marte');
        break;
    case 2:
        console.log('Tierra');
        break;
    case 3:
        console.log('Venus');
        break;
    case 4:
        console.log('Jupiter');
        break;
    case 5:
        console.log('Saturno');
        break;   
    case 6:
        console.log('Urano');
        break;
    case 7:
        console.log('Nepturno');
        break;
    default:
        console.log('No es un planeta');
}